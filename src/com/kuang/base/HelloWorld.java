package com.kuang.base;

/**
 * 可以用来自动创建文档的注释
 *
 * @ 写在类和方法的定义处
 * @ 用于自动创建文档
 */

// 类名与文件名相同
// class定义1个类  类名为Demo1_helloWorld，类名大写
// public是关键字
public class HelloWorld {

    // main方法  参数(String[] args)
    // void代表空，是方法的返回类型
    public static void main(String[] args) {

        // 打印字符串到屏幕
        System.out.println("Hello World");
    }
}
